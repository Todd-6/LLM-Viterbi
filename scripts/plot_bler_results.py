import os
import re
import matplotlib.pyplot as plt


def load_bler_results(txt_path):
    """
    Parse batch_test_results_*.txt and return sorted data:
    [(snr, std_bler, corr_bler, llm_bler), ...]
    """
    data = []
    if not os.path.exists(txt_path):
        print(f"Error: file not found: {txt_path}")
        return data

    line_re = re.compile(r"^\s*\d+")
    with open(txt_path, "r", encoding="utf-8") as f:
        for line in f:
            if not line_re.match(line):
                continue
            # Split by whitespace or pipe and remove empty parts
            parts = [p for p in re.split(r"\s*\|\s*|\s+", line.strip()) if p]
            if len(parts) < 5:
                continue

            try:
                snr = int(parts[0])
                tested = int(parts[1])
                std_err = int(parts[2])
                corr_err = int(parts[3])
                llm_err = int(parts[4])
            except ValueError:
                continue

            if tested <= 0:
                continue

            std_bler = std_err / tested
            corr_bler = corr_err / tested
            llm_bler = llm_err / tested
            data.append((snr, std_bler, corr_bler, llm_bler))

    data.sort(key=lambda x: x[0])
    return data


def plot_bler_results(txt_path, output_path):
    data = load_bler_results(txt_path)
    if not data:
        print("No valid data rows found.")
        return

    snr_vals = [d[0] for d in data]
    std_bler = [d[1] for d in data]
    corr_bler = [d[2] for d in data]
    llm_bler = [d[3] for d in data]

    plt.figure(figsize=(8, 8))
    blue = "#0000FF"
    red = "#FF0000"

    plt.plot(
        snr_vals,
        std_bler,
        label="Standard Viterbi",
        color="#000000",
        marker="x",
        linestyle="--",
        linewidth=2.5,
        markersize=7,
    )
    plt.plot(
        snr_vals,
        corr_bler,
        label="One-shot LLM Correction",
        color=blue,
        marker="s",
        linestyle="-",
        linewidth=2.5,
        markersize=7,
    )
    plt.plot(
        snr_vals,
        llm_bler,
        label="LLM Viterbi",
        color=red,
        marker="^",
        linestyle="-",
        linewidth=2.5,
        markersize=7,
    )

    plt.xlabel("SNR (dB)", fontsize=24)
    plt.ylabel("BLER", fontsize=24)
    plt.yscale("log")
    plt.legend(fontsize=22)
    plt.tick_params(axis="both", labelsize=20)
    plt.grid(True, linestyle="--", alpha=0.7)
    plt.xticks(snr_vals)

    plt.tight_layout()
    plt.savefig(output_path, dpi=300)
    print(f"Plot saved to {output_path}")


if __name__ == "__main__":
    _script_dir = os.path.dirname(os.path.abspath(__file__))
    _results_dir = os.path.abspath(os.path.join(_script_dir, "..", "results"))
    os.makedirs(_results_dir, exist_ok=True)

    # Input: batch_test_results_<timestamp>.txt generated by evaluation/batch_test_bler.py
    # Auto-select the latest result file in results/
    _candidates = sorted([
        f for f in os.listdir(_results_dir)
        if f.startswith("batch_test_results_") and f.endswith(".txt")
    ])
    if not _candidates:
        print("No batch_test_results_*.txt found in results/. Run evaluation/batch_test_bler.py first.")
        exit(1)
    txt_file = os.path.join(_results_dir, _candidates[-1])
    print(f"Using result file: {txt_file}")

    output_file = os.path.join(_results_dir, "bler_plot.png")
    plot_bler_results(txt_file, output_file)
